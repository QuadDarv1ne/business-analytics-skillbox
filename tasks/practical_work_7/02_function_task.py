"""
Задача №2. Функция

Программа вычисляет значение функции Y по следующим правилам:
- Если X > 0: Y = X - 12
- Если X = 0: Y = 5
- Если X < 0: Y = X²

На вход подаётся число X, программа выводит значение Y.
"""

x = int(input("Введите икс: "))

if x > 0:
    y = x - 12
elif x == 0:
    y = 5
else:
    y = x ** 2

print(f"Игрек равен {y}")